dataTable1 (DatE (Name n : ps) : (spanExps n -> (es, rest))) = (n, ps, es) : dataTable1 rest
dataTable1 (Dat (Name n : ps) (NormalCons cs) : rest) = (n, ps, map f cs) : dataTable1 rest
  where
    f :: DataCon -> (String, Typ)
    f (Con (Name nc) ts) = (nc, foldr (\x xs -> Sym x (NormalSym (Symb "->")) xs) conT ts)
    conT = foldl App (Iden (Name n)) (map Iden ps)
dataTable1 (Fam _ r@(splitInstHead -> (n, pst)) z : rest) = (n, ps, [e]) : dataTable1 rest
  where
    ps :: [Name]
    ps =
      pst
        ^.. template
          . filtered
            ( \case
                Name (x : _) -> isLower x
                _ -> False
            )
    e :: (String, Typ)
    e = case splitInstHead z of
      (x, xs) -> (x, arrT xs r)

