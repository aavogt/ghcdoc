splitInstHead :: Typ -> (String, [Typ])
splitInstHead = go []
  where
    go accum (App a b) = go (b : accum) a
    go accum (Iden (Name x)) = (x, accum)
    go accum (Sym a (SymBT (Name x)) b) = (x, a : b : accum)
    go x y = error $ show (x, y)

