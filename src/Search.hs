lookupQS :: (Ord a) => [NameQ] -> Map String (Map (Set Bool) (Set a)) -> Set a
lookupQS ns vpd =
  foldl (\result n -> lookupQ n vpd result) full $
    sortOn isNeg ns -- negatives last
  where
    full = S.unions $ vpd ^.. folded . folded
    isNeg (NameQ _ _ (Just (_, S.toList -> [x]))) = x
    isNeg _ = False

lookupQ ::
  (Ord a) =>
  -- | a single word like +A-
  NameQ ->
  Map String (Map (Set Bool) (Set a)) ->
  -- | modification of the result set
  (Set a -> Set a)
lookupQ NameQ {pol, name, pres} vpd = presOp pres $ S.unions (vpd ^.. ix name . polOp pol)

polOp :: Maybe (Bool, Set Bool) -> Traversal' (Map (Set Bool) (Set a)) (Set a)
polOp Nothing = traversed
polOp (Just nb) = itraversed . ifiltered (\i _ -> keep nb i)
  where
    keep :: (Bool, Set Bool) -> Set Bool -> Bool
    keep (True, q) b = q == b
    keep (False, q) b = not (S.null (q `S.intersection` b)) -- or setIsSubsetOf?

presOp :: (Ord a) => Maybe (Bool, Set Bool) -> (Set a -> Set a -> Set a)
presOp = \case
  Nothing -> S.intersection
  Just (_, S.toList -> [True]) -> (<>) -- not really useful but what else could the trailing + mean?
  Just (_, S.toList -> [False]) -> flip (S.\\)
  pm -> error $ "cannot interpret " ++ show pm ++ " as a set operation"

